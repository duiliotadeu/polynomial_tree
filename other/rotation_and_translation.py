    maximal_list = maximals

    for j in range(len(vertex_list[0])):
      for k in range(len(vertex_list[0][0])):
        if (j == 0 and k == 0):
           continue
        for maximal in maximals:
            to_remove = []
            for element in maximal:
              i = element-1
              to_remove.append(vertex_list[i][j][k])
            for maximal_new in maximal_list:
              if (set(maximal_new) == set(to_remove)):
                  print(f'{maximal}[{j}][{k}] = {maximal_new}')
                  maximal_list.remove(maximal_new)
    
    return maximal_list

